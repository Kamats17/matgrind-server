// Neutral attack cards that make no sense while trapped in a front headlock
const FHL_TRAPPED_EXCLUDED_IDS = new Set([
  'double_leg', 'single_leg', 'high_crotch', 'sweep_single', 'duck_under',
  'snap_spin', 'go_behind', 'arm_drag', 'fireman_carry', 're_shot',
  'stuff_head', 'down_block', 'whizzer', 'sprawl',
  'suplex', 'headlock_throw', 'lateral_drop', 'bear_hug_lift',
  // Greco-specific
  'pummel_inside', 'arm_throw', 'shuck_by',
  // Tie-up/chain cards
  'collar_tie', 'underhook_control', 'snap_down', 'inside_trip', 'drag_by', 'slide_by', 'duck_under',
]);

// When TIE_UP is active, these shot-based neutral cards are excluded
const TIE_UP_EXCLUDED_IDS = new Set([
  'double_leg', 'single_leg', 'high_crotch', 'sweep_single',
]);

// When LEG_ATTACK_SECURED is active, these neutral cards don't fit - you have a leg
const LEG_ATTACK_EXCLUDED_IDS = new Set([
  'collar_tie', 'underhook_control', 'snap_down', 'snap_spin',
  'go_behind', 'arm_drag', 're_shot', 'stuff_head', 'down_block',
  'pummel_inside', 'arm_throw', 'shuck_by',
]);

// When FRONT_HEADLOCK_CONTROL is active, only FHL branch cards make sense - exclude everything else
const FHL_CONTROL_EXCLUDED_IDS = new Set([
  'collar_tie', 'underhook_control',  // can't re-enter setup from FHL
  'whizzer', 'sprawl', 'down_block', 'stuff_head', // shot defenses, irrelevant from FHL
  'double_leg', 'single_leg', 'high_crotch', 'sweep_single', 'fireman_carry', // can't shoot from FHL
  'duck_under', 'slide_by', 'drag_by', 'snap_spin', // neutral attacks incompatible with FHL
  'arm_drag', 'go_behind', 're_shot', // you already have control
  'peek_out', // peek_out is for escaping FHL, not attacking from it
  // Greco-specific
  'pummel_inside', 'arm_throw', 'shuck_by',
  'suplex', 'headlock_throw', 'lateral_drop', 'bear_hug_lift',
]);

// When REAR_STANDING is active, these neutral cards don't fit - you're already behind them.
// headlock_throw needs front head control and lateral_drop needs a front over-under tie;
// neither works from behind. A belly-to-back suplex and rear body-lock lift do, so those stay.
const REAR_STANDING_EXCLUDED_IDS = new Set([
  'double_leg', 'single_leg', 'high_crotch', 'sweep_single',
  'collar_tie', 'underhook_control', 'snap_down', 'snap_spin',
  'fireman_carry', 're_shot', 'stuff_head', 'down_block',
  'headlock_throw', 'lateral_drop',
]);

// When LEG_RIDE_ESTABLISHED is active, you have legs hooked in - only leg-chain finishers + generic control moves fit.
// Exclude other top turns/rides that assume a different hold.
const LEG_RIDE_EXCLUDED_IDS = new Set([
  'get_legs_in', // can't re-establish the ride you already have
  'spiral_ride', 'ankle_ride', 'claw_ride', 'cross_wrist_ride', // alternative rides incompatible with legs-in
  'navy_ride', 'wrist_ride', 'tight_waist_chop', 'tight_waist', 'spiral_breakdown',
  'head_post', 'cross_face_pressure', 'pop_chop', 'chop_and_drive',
  'two_on_one_ride', // alternate ride setup
  // Standard turns that don't chain off legs specifically (leg-ride chain cards are preferred)
  'chicken_wing', 'double_arm_bar', 'bar_arm',
]);

export function getAvailableCards(position, conditions = [], style = 'folkstyle') {
  const isFhlTrapped = conditions.includes(CONDITIONS.FRONT_HEADLOCK_TRAPPED);
  const isLegTrapped = conditions.includes(CONDITIONS.LEG_ATTACK_TRAPPED);
  const isFhlControl = conditions.includes(CONDITIONS.FRONT_HEADLOCK_CONTROL);
  const isTieUp = conditions.includes(CONDITIONS.TIE_UP);
  const isRearStanding = conditions.includes(CONDITIONS.REAR_STANDING);
  const isRearTrapped = conditions.includes(CONDITIONS.REAR_STANDING_TRAPPED);
  const isLegAttack = conditions.includes(CONDITIONS.LEG_ATTACK_SECURED);
  const isLegRide = conditions.includes(CONDITIONS.LEG_RIDE_ESTABLISHED);

  // Women's freestyle uses the men's freestyle card pool by default. Cards
  // tagged 'womens_freestyle' (women's-specific signature moves) are also
  // included. When tuning women's mode further, cards can opt out of the
  // freestyle pool by tagging only ['womens_freestyle'].
  const styleMatches = (cardStyles) => {
    if (!cardStyles) return true;
    if (cardStyles.includes(style)) return true;
    if (style === 'womens_freestyle' && cardStyles.includes('freestyle')) return true;
    return false;
  };

  return Object.values(CARDS).filter(card => {
    // Style filter - only show cards that belong to this wrestling style
    if (!styleMatches(card.styles)) return false;

    // Trapped-state pools are resolved BEFORE the position filter. A trapped
    // wrestler is restricted to their dedicated defense pool regardless of the
    // `position` field: the defense cards are position:NEUTRAL, and a stale or
    // mismatched position must never empty the pool (which would fall back to
    // unrelated neutral cards). Style is already applied above.
    // When trapped in a front headlock, only FHL defense cards and peek_out are available
    if (isFhlTrapped) {
      return card.fhlDefense === true || card.id === 'peek_out';
    }

    // When trapped in a leg attack, only the dedicated leg-defense pool is
    // available. Six cards: limp_leg, whizzer_hop, crossface_and_circle,
    // front_headlock_counter, whizzer_lateral_drop, heavy_hips.
    if (isLegTrapped) {
      return card.legDefense === true;
    }

    // When trapped in a rear standing position, only the dedicated rear-defense
    // pool is available (rear_hand_fight, rear_hip_down, rear_whizzer_block,
    // and folkstyle-only rear_standing_switch / rear_peel_and_turn).
    if (isRearTrapped) {
      return card.rearDefense === true;
    }

    if (card.position !== null && card.position !== position) return false;

    // Exclude FHL defense cards unless the player has FRONT_HEADLOCK_TRAPPED
    if (card.fhlDefense === true) return false;

    // Exclude leg defense cards unless the player has LEG_ATTACK_TRAPPED
    if (card.legDefense === true) return false;

    // Exclude rear-defense cards unless the player has REAR_STANDING_TRAPPED
    if (card.rearDefense === true) return false;

    // Peek out only available when trapped in FHL or in a tie-up. Leg-trapped
    // has its own dedicated 6-card pool (limp_leg, whizzer_hop, crossface_and_circle,
    // front_headlock_counter, whizzer_lateral_drop, heavy_hips) so peek_out is
    // redundant filler there.
    if (card.id === 'peek_out' && !isFhlTrapped && !isTieUp) return false;

    // When controlling a front headlock, only FHL branch cards + snap_down are relevant
    if (isFhlControl && FHL_CONTROL_EXCLUDED_IDS.has(card.id)) return false;

    // When the leg ride is established, only leg-chain finishers make sense from TOP
    if (isLegRide && LEG_RIDE_EXCLUDED_IDS.has(card.id)) return false;
    if (isLegRide && card.scoreEffect?.setsCondition === CONDITIONS.LEG_RIDE_ESTABLISHED) return false;

    // When in a tie-up, hide cards that SET TIE_UP (can't re-enter collar tie)
    if (isTieUp && card.scoreEffect?.setsCondition === CONDITIONS.TIE_UP) return false;

    // When leg attack secured, exclude incompatible neutral attacks - you have a leg
    if (isLegAttack && LEG_ATTACK_EXCLUDED_IDS.has(card.id)) return false;
    if (isLegAttack && card.scoreEffect?.setsCondition === CONDITIONS.LEG_ATTACK_SECURED) return false;
    // Can't throw for amplitude or re-tie while one of their legs is in your hands
    if (isLegAttack && card.scoreEffect?.type === 'grand_amplitude') return false;
    if (isLegAttack && card.scoreEffect?.setsCondition === CONDITIONS.TIE_UP) return false;

    // When in rear standing, exclude incompatible neutral attacks
    if (isRearStanding && REAR_STANDING_EXCLUDED_IDS.has(card.id)) return false;
    if (isRearStanding && card.scoreEffect?.setsCondition === CONDITIONS.TIE_UP) return false;
    if (isRearStanding && card.scoreEffect?.setsCondition === CONDITIONS.REAR_STANDING) return false;

    // When in a tie-up, exclude shot-based takedowns that don't fit
    if (isTieUp && TIE_UP_EXCLUDED_IDS.has(card.id)) return false;

    if (card.setupRequired && card.setupRequired.length > 0) {
      return card.setupRequired.every(req => conditions.includes(req));
    }
    return true;
  });
}

export function getCardById(id) {
  return CARDS[id] || null;
}

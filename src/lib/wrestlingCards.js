// Wrestling Card Definitions
// Folkstyle: USA Wrestling folkstyle curriculum - NFHS rules
// Freestyle: UWW international freestyle rules
// Greco-Roman: UWW Greco-Roman rules - no holds below the hips

export const POSITIONS = {
  NEUTRAL: 'neutral',
  TOP: 'top',
  BOTTOM: 'bottom',
};

export const CONDITIONS = {
  BROKEN_DOWN: 'broken_down',
  CONTROL_ESTABLISHED: 'control_established',
  TOP_PRESSURE: 'top_pressure',
  FRONT_HEADLOCK_CONTROL: 'front_headlock_control',
  FRONT_HEADLOCK_TRAPPED: 'front_headlock_trapped', // Defender being held in the front headlock
  RECOVERING: 'recovering',
  GOOD_BASE: 'good_base',             // Bottom wrestler built a solid base by surviving turn attempts
  HAND_FIGHTING: 'hand_fighting_control', // Neutral wrestler winning the hand-fighting battle
  INSIDE_POSITION: 'inside_position',     // Greco: underhook advantage - powers up throws
  LEG_ATTACK_SECURED: 'leg_attack_secured',   // Attacker has a leg - needs to finish the takedown
  LEG_ATTACK_TRAPPED: 'leg_attack_trapped',   // Defender's leg is caught - needs to free it
  SCRAMBLE: 'scramble',                       // Both wrestlers tangled in a scramble
  TIE_UP: 'tie_up',                           // Neutral tie-up established - unlocks chain attacks
  REAR_STANDING: 'rear_standing',             // Attacker behind opponent standing - needs mat return to finish
  REAR_STANDING_TRAPPED: 'rear_standing_trapped', // Defender has someone behind them - must fight the rear position
  BASE_BUILT: 'base_built',                   // Bottom wrestler built a base - unlocks stand-up escape
  LEG_RIDE_ESTABLISHED: 'leg_ride_established', // Top wrestler has legs hooked inside opponent's - unlocks leg-ride chain (folkstyle)
};

